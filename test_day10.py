import pytest

from day10 import is_valid_parantheses


@pytest.mark.parametrize(
    "value, exp",
    [
        ("a", []),
        (")", ")"),
        ("]", "]"),
        ("}", "}"),
        (">", ">"),
        ("()", []),
        ("[]", []),
        ("{}", []),
        ("<>", []),
        ("<abc>", []),
        ("<abc>a", []),
        ("a<abc>a", []),
        ("a<abc>", []),
        ("{)", ")"),
        ("<]", "]"),
        ("<>]", "]"),
        ("[<>]", []),
        ("[<()>]", []),
        ("[<(>)]", ">"),
        ("{([(<{}[<>[]}>{[]{[(<()>", "}"),
        ("[[<[([]))<([[{}[[()]]]", ")"),
        ("[{[{({}]{}}([{[{{{}}([]", "]"),
        ("[<(<(<(<{}))><([]([]()", ")"),
        ("<{([([[(<>()){}]>(<<{{", ">"),
        ("[<>({}){}[([])<>]]", []),
        ("(((((((((())))))))))", []),
        ("[({(<(())[]>[[{[]{<()<>>", list(reversed("}}]])})]"))),
        ("[(()[<>])]({[<{<<[]>>(", list(reversed(")}>]})"))),
        ("(((({<>}<{<{<>}{[]{[]{}", list(reversed("}}>}>))))"))),
        ("{<[[]]>}<{[{[{[]{()[[[]", list(reversed("]]}}]}]}>"))),
        ("<{([{{}}[<[[[<>{}]]]>[]]", list(reversed("])}>"))),
    ],
)
def test_is_valid_parentheses(value, exp):
    # GIVEN
    """
    input: value
    expected result: exp
    """

    # WHEN
    res = is_valid_parantheses(value)

    # THEN
    assert res == exp
